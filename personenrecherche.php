                Eine Besonderheit in der Recherche zu NSDAP-Mitgliedern liegt darin, dass die Namen hinter Signaturen zu den Zentral- und Gaukarteien (R 9361-VIII bzw. R 9361-IX) über die elektronische Suchhilfe Invenio nicht zu finden sind. Auch ein Findbuch existiert im Lesesaal nicht. Demzufolge müssen die Bestände mit Hilfe einer Anwendung manuell durchsucht werden. Die die NSDAP-Karteien liegen für Nutzende “digitalisiert” in Form von Fotos vor. Umständlich gestaltet sich die Suche in dieser Anwendung, weil nur etwa jede 50. Kartei aus der entsprechenden Datenbank verzeichnet ist. Bei mehr als 12,7 Millionen überlieferten Karteikarten (ca. 4,4 Millionen aus der Zentral-Kartei, ca. 8,3 Millionen aus der Gau-Kartei) sind demzufolge über 12,4 Millionen Karteikarten nicht für die Suche erfasst!
